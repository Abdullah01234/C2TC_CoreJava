 	
public class TernaryOperator {

	public static void main(String[] args) {
		
		int v=1;
//Whenever you have to this question mark(?) and colon(:) together then called as Ternary Operator
// What is this ternary Operator will do is? it's checks the value before your Question mark(?)and it given boolean output 
//Tell me V==1 is it true/False = True, if your output is TRUE, before your question mark(?),Then it Consider the value before a colon(:); Output of the first printing statement is A.
		System.out.println(v==1?"A":"B"); //T? //A
		
//tell me V==2 is it True/false =False, if your output is FALSE, before your question mark(?), Then consider the value after a colon(:); Output of the Second printing statement is B.
		System.out.println(v==2?"A":"B"); //F? //B
		
//Output
//A
//B

	}

}
